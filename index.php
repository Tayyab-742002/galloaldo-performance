    function showStep(step) {
        // Nascondi tutti gli step
        var steps = document.getElementsByClassName('step');
        for (var i = 0; i < steps.length; i++) {
            steps[i].classList.remove('active');
        }
        
        // Mostra lo step corrente
        steps[step].classList.add('active');
        
        // Aggiorna la barra di progresso
        updateProgressBar(step);
    }

    function nextStep(currentStep) {
        // Valida i campi obbligatori prima di procedere
        if (validateStep(currentStep)) {
            showStep(currentStep + 1);
            
            // Se siamo all'ultimo step, mostra il riepilogo
            if (currentStep + 1 === 3) {
                reviewInputs();
            }
        }
    }

    function prevStep(currentStep) {
        showStep(currentStep - 1);
    }

    function validateStep(step) {
        // Validazione base - puoi espanderla
        var inputs = document.querySelectorAll('.step.active input[required], .step.active select[required], .step.active textarea[required]');
        for (var i = 0; i < inputs.length; i++) {
            if (!inputs[i].value) {
                alert('Per favore compila tutti i campi obbligatori');
                inputs[i].focus();
                return false;
            }
        }
        return true;
    }

