        var vid = el.getAttribute('data-vid');
        var title = el.getAttribute('data-title') || 'YouTube video';
        var iframe = document.createElement('iframe');
        iframe.setAttribute('src','https://www.youtube.com/embed/'+vid+'?autoplay=1&rel=0');
