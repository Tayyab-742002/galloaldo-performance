            </div><h2>Aste per bandiere da esterno, i materiali</h2>
<p>Avremmo potuto offrire aste per bandiere in fibra di vetro, in acciaio,  abbiamo scelto l'alluminio per due ragioni fondamentali: il peso e la flessibilità. Le aste in alluminio sono leggere, facili da trsportare e rispetto alla fibra di vetro decisamente meno impegnative per il costo economico. Ancora, le aste in fibra di vetro devono necessariamente essere costituite da un unico pezzo, quindi richiedono un trasporto dedicato. Le nostre aste in alluminio sono in più pezzi proprio per garantire un trasporto facile. Ancora, le aste in alluminio sono flessibili. Questo è un elemento fondamentale per sostenere una bandiera.</p>
